#!/usr/bin/env python3
"""Encode ProcessBench GSM8K split with the SSAE encoder.

ProcessBench (Qwen/ProcessBench, split='gsm8k') contains 400 multi-step math
solutions generated by Qwen2-7B-Instruct on GSM8K problems. Each solution is
annotated with the 0-indexed position of the first incorrect step, or -1 if
all steps are correct.

Per-step binary labels (for classification comparison with Math-Shepherd):
  - Correct solutions (label == -1): every step labeled 1 (correct).
  - Incorrect solutions (label == k): steps 0..k-1 labeled 1 (correct),
    step k labeled 0 (incorrect). Steps after k are excluded because their
    correctness is ambiguous once an earlier step has gone wrong.

Encoding uses the same pipeline as generate_probe_data.py:
  [context tokens] + [<sep>] + [step tokens] + [<eos>]
  context = problem + all prior steps

Output .npz:
  latents          float16 (N, 896)   -- SSAE latents for each encoded step
  step_labels      int8    (N,)       -- 0=incorrect, 1=correct
  solution_ids     int32   (N,)       -- row index in ProcessBench (0..399)
  step_positions   int8    (N,)       -- 0-indexed position within solution
  solution_labels  int32   (400,)     -- original label per row (-1 or first-error idx)
  n_steps_encoded  int32   (400,)     -- how many steps were encoded per solution

Usage:
    python scripts/encode_processbench.py \\
        --checkpoint gsm8k-385k_Qwen2.5-0.5b_spar-10.pt \\
        --output results/probe_data/processbench_gsm8k.npz \\
        --device cuda
"""

import argparse
import sys
from pathlib import Path

import numpy as np
import torch
from datasets import load_dataset
from tqdm import tqdm

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from src.saes.ssae import SSAE


def build_records(row: dict, row_idx: int) -> list[dict]:
    """Convert one ProcessBench row into per-step encoding records.

    Includes steps up to and including the first error (or all steps if correct).
    Steps after the first error are excluded.
    """
    problem = row["problem"]
    steps = row["steps"]
    label = row["label"]  # -1 = all correct, k = 0-indexed first error position

    n_include = len(steps) if label == -1 else label + 1
    records = []
    prior: list[str] = []

    for i in range(min(n_include, len(steps))):
        context = (problem + " " + " ".join(prior)).strip() if prior else problem
        step_correct = 1 if (label == -1 or i < label) else 0
        records.append({
            "context": context,
            "text": steps[i],
            "step_label": step_correct,
            "step_pos": i,
            "solution_id": row_idx,
        })
        prior.append(steps[i])

    return records


def encode_batch(model, tokenizer, contexts, steps, device, sep_token_id, max_seq_len=2048):
    batch_ids = []
    for ctx, step in zip(contexts, steps):
        ctx_ids = tokenizer.encode(ctx, add_special_tokens=False)
        step_ids = tokenizer.encode(step, add_special_tokens=False)
        seq = ctx_ids + [sep_token_id] + step_ids + [tokenizer.eos_token_id]
        if len(seq) > max_seq_len:
            keep = max_seq_len - len(step_ids) - 2
            ctx_ids = ctx_ids[-max(keep, 0):]
            seq = ctx_ids + [sep_token_id] + step_ids + [tokenizer.eos_token_id]
        batch_ids.append(seq)

    max_len = max(len(s) for s in batch_ids)
    pad_id = tokenizer.eos_token_id
    input_ids = torch.full((len(batch_ids), max_len), pad_id, dtype=torch.long, device=device)
    attn_mask = torch.zeros((len(batch_ids), max_len), dtype=torch.long, device=device)
    for i, seq in enumerate(batch_ids):
        input_ids[i, :len(seq)] = torch.tensor(seq, dtype=torch.long, device=device)
        attn_mask[i, :len(seq)] = 1

    with torch.no_grad():
        latents = model.encode(input_ids, attn_mask)
    return latents.squeeze(1).cpu().float().numpy()


def parse_args():
    p = argparse.ArgumentParser()
    p.add_argument("--checkpoint", required=True,
                   help="Path to SSAE checkpoint .pt file")
    p.add_argument("--split", default="gsm8k",
                   choices=["gsm8k", "math", "olympiadbench", "omnimath"],
                   help="ProcessBench split to encode")
    p.add_argument("--output", default="results/probe_data/processbench_gsm8k.npz")
    p.add_argument("--batch-size", type=int, default=16)
    p.add_argument("--max-seq-len", type=int, default=2048)
    p.add_argument("--device", default="cuda", choices=["cpu", "cuda", "mps"])
    p.add_argument("--cache-dir", default=None,
                   help="HuggingFace cache dir (for offline use on compute nodes)")
    return p.parse_args()


def main():
    args = parse_args()

    model = SSAE.from_checkpoint(args.checkpoint, device=args.device)
    model.eval()
    tokenizer = model.tokenizer
    sep_tok_id = tokenizer.sep_token_id

    print(f"Loading ProcessBench (Qwen/ProcessBench, split='{args.split}')...")
    ds = load_dataset(
        "Qwen/ProcessBench",
        split=args.split,
        cache_dir=args.cache_dir,
    )
    n_solutions = len(ds)
    print(f"  Solutions loaded: {n_solutions}")

    # Collect all per-step records and store solution-level metadata.
    all_records: list[dict] = []
    solution_labels = np.full(n_solutions, -2, dtype=np.int32)
    n_steps_encoded = np.zeros(n_solutions, dtype=np.int32)

    for row_idx, row in enumerate(ds):
        solution_labels[row_idx] = row["label"]
        recs = build_records(row, row_idx)
        n_steps_encoded[row_idx] = len(recs)
        all_records.extend(recs)

    n_total = len(all_records)
    n_correct_sols = int((solution_labels == -1).sum())
    n_incorrect_sols = n_solutions - n_correct_sols
    n_step_correct = sum(r["step_label"] for r in all_records)
    n_step_incorrect = n_total - n_step_correct

    print(f"\nDataset summary:")
    print(f"  Solutions        : {n_solutions}")
    print(f"    Correct  (-1)  : {n_correct_sols}  ({n_correct_sols/n_solutions:.1%})")
    print(f"    Incorrect      : {n_incorrect_sols}  ({n_incorrect_sols/n_solutions:.1%})")
    print(f"  Steps encoded    : {n_total}")
    print(f"    Correct  (+)   : {n_step_correct}  ({n_step_correct/n_total:.1%})")
    print(f"    Incorrect (-)  : {n_step_incorrect}  ({n_step_incorrect/n_total:.1%})")
    print(f"  Steps/solution   : {n_total/n_solutions:.1f} avg")
    print(f"  Majority baseline (step level): {max(n_step_correct, n_step_incorrect)/n_total:.1%}")

    # Encode in batches.
    all_latents: list[np.ndarray] = []
    bs = args.batch_size
    max_seq = args.max_seq_len if args.max_seq_len > 0 else 10**9

    for i in tqdm(range(0, n_total, bs), desc="Encoding with SSAE"):
        batch = all_records[i:i + bs]
        lats = encode_batch(
            model, tokenizer,
            [r["context"] for r in batch],
            [r["text"] for r in batch],
            args.device, sep_tok_id, max_seq_len=max_seq,
        )
        all_latents.append(lats)

    latents_arr = np.concatenate(all_latents, axis=0).astype(np.float16)
    step_labels_arr = np.array([r["step_label"] for r in all_records], dtype=np.int8)
    solution_ids_arr = np.array([r["solution_id"] for r in all_records], dtype=np.int32)
    step_positions_arr = np.array([r["step_pos"] for r in all_records], dtype=np.int8)

    out = Path(args.output)
    out.parent.mkdir(parents=True, exist_ok=True)
    np.savez_compressed(
        out,
        latents=latents_arr,
        step_labels=step_labels_arr,
        solution_ids=solution_ids_arr,
        step_positions=step_positions_arr,
        solution_labels=solution_labels,
        n_steps_encoded=n_steps_encoded,
    )
    print(f"\nSaved {n_total} encoded steps -> {out}")
    print(f"Array shapes: latents={latents_arr.shape}  step_labels={step_labels_arr.shape}")


if __name__ == "__main__":
    main()
